package Models;

/**
 *
 * @author tuant
 */
public class Brand {
    private int id;
    private String name;
    private int brandNumber;

    public Brand() {
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getBrandNumber() {
        return brandNumber;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setBrandNumber(int brandNumber) {
        this.brandNumber = brandNumber;
    }
    
    
    
}
